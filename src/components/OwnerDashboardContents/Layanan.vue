<template>
  <v-data-table :headers="headers" :items="layanans" :search="keyword" :loading="load">
    <template v-slot:body="{ items }">
      <tbody>
        <tr v-for="item in items" :key="item.idLayanan">
          <td>{{ item.idLayanan }}</td>
          <td>{{item.namaLayanan}}</td>
          <td>{{item.harga}}</td>
          <td>{{ item.namaUkuran }}</td>
          <td>{{ item.idPegawaiLog }}</td>
          <td>
            <v-btn icon color="indigo" light @click="editHandler(item)">
              <v-icon>mdi-pencil</v-icon>
            </v-btn>
            <v-btn icon color="error" light @click="deleteData(item.idUkuran)">
              <v-icon>mdi-delete</v-icon>
            </v-btn>
          </td>
        </tr>
      </tbody>
    </template>
    <!-- <template v-slot:top> -->
    <v-toolbar>
      <v-toolbar-title>Data layanan</v-toolbar-title>
      <v-divider class="mx-4" inset vertical></v-divider>
      <v-spacer />
      <v-flex xs5 class="text-right">
        <v-text-field v-model="keyword" append-icon="mdi-search" label="Search" hide-details></v-text-field>
      </v-flex>
      <v-spacer />
      <v-divider class="mx-4" inset vertical></v-divider>
      <v-btn rounded @click="dialog=true" color="green accent-3">
        <v-icon size="18" class="mr-1">mdi-pencil-plus</v-icon>Tambah layanan
      </v-btn>
    </v-toolbar>

    <v-dialog v-model="dialog" presistent max-width="400">
      <v-card>
        <v-card-text>
          <v-container>
            <v-row>
              <v-col cols="12">
                <v-text-field
                  prepend-icon="mdi-rename-box"
                  label="Nama layanan*"
                  v-model="form.namaLayanan"
                  required
                ></v-text-field>
              </v-col>

              <v-col cols="12">
                <v-text-field
                  prepend-icon="mdi-cash-usd"
                  label="Harga*"
                  v-model="form.harga"
                  required
                ></v-text-field>
              </v-col>

              <v-col cols="12">
                <v-text-field
                  prepend-icon="mdi-format-size"
                  label="ID Ukuran*"
                  v-model="form.idUkuran"
                  required
                ></v-text-field>
              </v-col>
            </v-row>
          </v-container>
          <small>*indicates required field</small>
        </v-card-text>
        <v-card-actions>
          <v-spacer></v-spacer>
          <v-btn color="red darken-1" text @click="dialog=false, resetForm()">Cancel</v-btn>
          <v-btn color="blue darken-1" text @click="setForm()">Save</v-btn>
        </v-card-actions>
      </v-card>
    </v-dialog>
    <!-- </template>
    <template v-slot:item.actions="{ item }">
      <v-icon color="indigo" class="mr-2" @click="editHandler(item)">mdi-pencil</v-icon>
      <v-icon color="error" @click="deleteData(item.idLayanan)">mdi-delete</v-icon>
    </template>-->
  </v-data-table>
</template>

<script>
export default {
  data: () => ({
    dialog: false,
    items: [],
    keyword: "",
    headers: [
      {
        text: "ID layanan",
        value: "idLayanan"
      },
      {
        text: "Nama layanan",
        value: "namaLayanan"
      },
      {
        text: "Harga",
        value: "harga"
      },
      {
        text: "Nama Ukuran",
        value: "idUkuran"
      },
      {
        text: "Ditambahkan Oleh",
        value: "idPegawaiLog"
      },
      {
        text: "Action",
        value: "actions",
        sortable: false
      }
    ],
    layanans: [],
    form: {
      namaLayanan: "",
      harga: "",
      idUkuran: "",
      idPegawaiLog: "Owner"
    },
    layanan: new FormData(),
    typeInput: "new",
    ukurans: [
      {
        namaUkuran: ""
      }
    ]
  }),

  methods: {
    getData() {
      var uri = this.$apiUrl + "layanan";
      this.$http.get(uri, this.layanan).then(response => {
        this.layanans = response.data.data;
      });
    },

    sendData() {
      this.layanan.append("namaLayanan", this.form.namaLayanan);
      this.layanan.append("harga", this.form.harga);
      this.layanan.append("idUkuran", this.form.idUkuran);
      this.layanan.append("idPegawaiLog", this.form.idPegawaiLog);

      var uri = this.$apiUrl + "layanan";
      this.load = true;
      this.$http
        .post(uri, this.layanan)
        .then(this.getData(), this.resetForm(), (this.load = false));
    },

    updateData() {
      this.layanan.append("namaLayanan", this.form.namaLayanan);
      this.layanan.append("harga", this.form.harga);
      this.layanan.append("idUkuran", this.form.idUkuran);
      this.layanan.append("idPegawaiLog", this.form.idPegawaiLog);

      var uri = this.$apiUrl + "layanan/" + this.idLayanan;
      this.load = true;
      this.$http
        .post(uri, this.layanan)
        .then(this.getData(), this.resetForm(), (this.load = false));
    },

    deleteData(idLayanan) {
      var uri = this.$apiUrl + "layanan/" + idLayanan; //data dihapus berdasarkan id
      this.$http
        .delete(uri, this.layanans)
        .then(this.getData(), this.resetForm(), (this.load = false));
    },

    editHandler(item) {
      this.typeInput = "edit";
      this.dialog = true;
      this.idLayanan = item.idLayanan;
      this.form.namaLayanan = item.namaLayanan;
      this.form.harga = item.harga;
      this.form.idUkuran = item.idUkuran;
    },

    setForm() {
      if (this.typeInput === "new") {
        this.sendData();
        this.dialog = false;
      } else {
        this.updateData();
        this.dialog = false;
      }
    },

    resetForm() {
      this.form = {
        namaLayanan: "",
        harga: "",
        idUkuran: "",
        idPegawaiLog: "Owner"
      };
    }
  },

  mounted() {
    this.getData();
  }
};
</script>